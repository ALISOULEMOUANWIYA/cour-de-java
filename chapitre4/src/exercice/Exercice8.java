public class Exercice8 {

	public static void main(String[] args) {
		// Declaration des variables
		char c;
		for(c = 'a'; c <= 'z'; c++) {
			System.out.println(c+" ");
		}
		System.out.println("---------");
		for(c = 'z';c >= 'a'; c--) {
			System.out.println(c+" ");
		}

	}

}
